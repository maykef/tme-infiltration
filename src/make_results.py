#!/usr/bin/env python
"""Stage 7 — assemble RESULTS.md from the JSON/log artifacts produced by Stages 3-6."""
import os
import sys
import json
import glob

sys.path.insert(0, os.path.dirname(__file__))
from utils import REPO


def load(p, default=None):
    try:
        with open(p) as f:
            return json.load(f)
    except Exception:
        return default


def read_runlog():
    times = {}
    path = os.path.join(REPO, "results", "run_log.jsonl")
    if os.path.exists(path):
        for line in open(path):
            r = json.loads(line)
            times.setdefault(r["stage"], []).append(r)
    return times


def main():
    R = os.path.join(REPO, "results")
    primary = load(os.path.join(R, "eval_results.json"), {})
    frozen = load(os.path.join(R, "eval_results_frozen.json"), {})
    nopre = load(os.path.join(R, "eval_results_nopretrain.json"), {})
    findings = load(os.path.join(R, "counterfactuals", "_top_findings.json"), {"findings": []})
    times = read_runlog()

    n_jackson = len(glob.glob(os.path.join(REPO, "data", "processed", "jackson", "*.pt")))
    n_mold = len(glob.glob(os.path.join(REPO, "data", "processed", "moldoveanu", "*.pt")))

    # wall-clock (take the last/most-relevant run of each)
    pre = [r for r in times.get("pretrain", []) if r["epochs_ran"] > 5]
    pre_s = pre[-1]["seconds"] if pre else float("nan")
    pre_ep = pre[-1]["best_epoch"] if pre else "?"
    ft = [r for r in times.get("finetune_eval", []) if not r.get("no_pretrain") and not r.get("freeze_trunk")]
    ft_s = ft[-1]["seconds"] if ft else float("nan")
    cf = times.get("counterfactual", [])
    cf_s = cf[-1]["seconds"] if cf else float("nan")

    def ci(d, key="gnn"):
        a = d.get(f"{key}_auc"); c = d.get(f"{key}_ci", [None, None])
        return a, c

    g_auc, g_ci = ci(primary, "gnn")
    b_auc, b_ci = ci(primary, "baseline")
    fr_auc, fr_ci = ci(frozen, "gnn")
    np_auc, np_ci = ci(nopre, "gnn")

    def fmt(a, c):
        if a is None:
            return "n/a"
        return f"{a:.3f}  (95% CI [{c[0]:.3f}, {c[1]:.3f}])"

    labels = primary.get("labels", [])
    n_resp = sum(labels); n_non = len(labels) - n_resp
    verdict = primary.get("verdict", "")

    lines = []
    A = lines.append
    A("# RESULTS — TME spatial-graph infiltration predictor\n")
    A("_Auto-generated by `src/make_results.py` from `results/*.json` and `results/run_log.jsonl`._\n")

    A("## 1. Datasets used (after filtering)\n")
    A(f"- **Pretraining — Jackson & Fischer 2020 (breast IMC):** {n_jackson} slide graphs, "
      f"285,851 cells, **33 markers** (45 channels minus 7 Ru counterstain, 2 DNA "
      f"intercalators, 3 all-NaN `exprs` channels). Self-supervised, no outcome labels.")
    A(f"- **Fine-tune + eval — Moldoveanu 2022 (melanoma ICI IMC):** {n_mold} slide graphs "
      f"= 30 patients (one slide each), **{n_resp} responders / {n_non} non-responders**, "
      f"**35 markers** (38 channels minus 3 technical). 112,497 cells.")
    A(f"- Graphs: k-NN (k=12) in (x,y), undirected; node features arcsinh + per-dataset "
      f"z-scoring (panels never pooled). Evaluation is **leave-one-patient-out** (30 folds).\n")

    A("## 2. Headline result — GNN vs. Giuliani pair-correlation baseline\n")
    A("| Model | AUROC (LOPO) |")
    A("|---|---|")
    A(f"| **Spatial-graph GNN** (pretrained trunk + fine-tune) | **{fmt(g_auc, g_ci)}** |")
    A(f"| **Giuliani baseline** — cross-PCF g(macro.mono ~ Tc.ae) @ r=10.5 µm | **{fmt(b_auc, b_ci)}** |")
    A("")
    A(f"**{verdict}.** The hand-crafted spatial statistic (macrophage/monocyte ↔ activated "
      f"CD8⁺ T-cell co-localization at 10.5 µm) clearly separates responders from "
      f"non-responders, while the learned GNN sits near chance at this sample size. This is "
      f"reported plainly: at N=30 with cross-panel (breast→melanoma) transfer, the GNN does "
      f"not beat the targeted biological feature.\n")

    A("### Sensitivity / ablation (transparent — not used to pick the headline)\n")
    A("| GNN configuration | AUROC (LOPO) |")
    A("|---|---|")
    A(f"| Pretrained trunk + fine-tune (**headline**) | {fmt(g_auc, g_ci)} |")
    A(f"| Pretrained trunk **frozen** | {fmt(fr_auc, fr_ci)} |")
    A(f"| **No pretraining** (random init) | {fmt(np_auc, np_ci)} |")
    A("")
    A("All three GNN variants are near chance with wide, 0.5-crossing CIs, so the conclusion "
      "is robust to configuration. Notably pretraining did **not** help (no-pretrain ≥ "
      "pretrained), i.e. the breast-cancer masked-marker representation did not transfer "
      "usefully to melanoma response — a clean negative transfer result.\n")

    A("## 3. Wall-clock (RTX PRO 6000 Blackwell, sm_120)\n")
    A(f"- Pretraining (Jackson, masked-marker, early-stopped @ epoch {pre_ep}): **{pre_s:.1f} s**")
    A(f"- Fine-tuning + LOPO eval (30 folds + baseline + 2000× bootstrap): **{ft_s:.1f} s**")
    A(f"- Counterfactual gradient search (16 non-responder slides): **{cf_s:.1f} s**")
    A("- Graph construction (130 slides, 32-way joblib): ~4 s. Env build + data download "
      "were parallelized during setup.\n")

    A("## 4. Top-5 counterfactual findings\n")
    A("Gradient of predicted P(response) w.r.t. per-cell marker features on non-responder "
      "slides; cells ranked by gradient magnitude, dominant marker shift named:\n")
    for i, f in enumerate(findings.get("findings", []), 1):
        A(f"{i}. {f}")
    gts = findings.get("global_top_marker_shifts", {})
    if gts:
        A("")
        A("**Most frequent dominant marker shift across the 16 non-responder slides:** "
          + ", ".join(f"`{k}` ({v})" for k, v in gts.items()) + ".")
    A("")
    A("_Interpretation: the model most often 'wants' exhaustion/naïveté-associated shifts "
      "(TIM3, CD45RA, CCR7) on melanoma and macrophage cells to raise predicted response — "
      "consistent with immune-context remodeling, but read cautiously given the GNN's "
      "near-chance discrimination above._\n")

    A("## 5. Reproduce\n")
    A("```bash\nbash scripts/build_env.sh   # once\nbash run_all.sh            # Stages 1..6 -> this file\n```")
    A("All scripts take `--seed` (default 42); every run is logged to "
      "`results/run_log.jsonl`. See `docs/data_schema.md` for the data contract and "
      "`docs/BUILD_SPEC.md` for the original plan.")

    out = os.path.join(REPO, "RESULTS.md")
    with open(out, "w") as f:
        f.write("\n".join(lines) + "\n")
    print("wrote", out)


if __name__ == "__main__":
    main()
