#!/usr/bin/env python
"""Stage 7 / correction — assemble RESULTS.md from JSON/log artifacts.

Shows the original 100-image Basel-subset run (archived in results/basel100/) side by side
with the corrected full-dataset (Basel+Zurich) run. Counterfactual findings are gated: they
are only presented as biological findings if the corrected GNN AUROC's lower CI clears the
baseline AUROC; otherwise they are shown under an explicit "NOT biological findings" caveat.
"""
import os
import sys
import json
import glob

sys.path.insert(0, os.path.dirname(__file__))
from utils import REPO


def load(p, default=None):
    try:
        return json.load(open(p))
    except Exception:
        return default


def read_runlog():
    times = {}
    path = os.path.join(REPO, "results", "run_log.jsonl")
    if os.path.exists(path):
        for line in open(path):
            r = json.loads(line)
            times.setdefault(r["stage"], []).append(r)
    return times


def fmt(a, c):
    if a is None:
        return "n/a"
    return f"{a:.3f} [{c[0]:.3f}, {c[1]:.3f}]"


def main():
    R = os.path.join(REPO, "results")
    # corrected (current) run
    cur = {t: load(os.path.join(R, f"eval_results{'_'+t if t else ''}.json"), {})
           for t in ["", "frozen", "nopretrain"]}
    # original 100-image run
    old = {t: load(os.path.join(R, "basel100", f"eval_results{'_'+t if t else ''}.json"), {})
           for t in ["", "frozen", "nopretrain"]}
    findings = load(os.path.join(R, "counterfactuals", "_top_findings.json"), {"findings": []})
    times = read_runlog()
    jmeta = load(os.path.join(REPO, "data", "jackson_processed", "jackson_meta.json"), {})
    old_jmeta = load(os.path.join(R, "basel100", "jackson_meta.json"), {})
    overlap = None
    ov_path = os.path.join(REPO, "docs", "marker_overlap.md")

    n_jackson = len(glob.glob(os.path.join(REPO, "data", "processed", "jackson", "*.pt")))
    n_mold = len(glob.glob(os.path.join(REPO, "data", "processed", "moldoveanu", "*.pt")))
    try:
        n_jmarkers = len(open(os.path.join(REPO, "data", "processed", "jackson",
                                           "_markers.txt")).read().split())
    except Exception:
        n_jmarkers = "?"
    # pull shared-count / Jaccard from docs/marker_overlap.md
    ov_shared, ov_jac = "?", "?"
    try:
        ovtxt = open(os.path.join(REPO, "docs", "marker_overlap.md")).read()
        import re as _re
        m1 = _re.search(r"Synonym-normalized shared:\s*\*\*(\d+)\*\*", ovtxt)
        m2 = _re.search(r"Jaccard[^:]*:\s*([\d.]+)%", ovtxt)
        if m1:
            ov_shared = m1.group(1)
        if m2:
            ov_jac = m2.group(1)
    except Exception:
        pass

    # latest pretrain / finetune / counterfactual timings
    pre = [r for r in times.get("pretrain", []) if r["epochs_ran"] > 5]
    pre_s, pre_ep, pre_g = (pre[-1]["seconds"], pre[-1]["best_epoch"], pre[-1]["n_graphs"]) if pre else ("?","?","?")
    ftc = [r for r in times.get("finetune_eval", []) if not r.get("no_pretrain") and not r.get("freeze_trunk")]
    ft_s = ftc[-1]["seconds"] if ftc else float("nan")
    cf = times.get("counterfactual", [])
    cf_s = cf[-1]["seconds"] if cf else float("nan")

    def auc(d): return d.get("gnn_auc"), d.get("gnn_ci", [None, None])
    def bauc(d): return d.get("baseline_auc"), d.get("baseline_ci", [None, None])

    g_auc, g_ci = auc(cur[""])
    b_auc, b_ci = bauc(cur[""])
    labels = cur[""].get("labels", [])
    n_resp = sum(labels); n_non = len(labels) - n_resp

    # ---- counterfactual gating: does corrected GNN lower-CI clear the baseline point? ----
    clears = (g_ci[0] is not None and b_auc is not None and g_ci[0] > b_auc)

    L = []
    A = L.append
    A("# RESULTS — TME spatial-graph infiltration predictor\n")
    A("_Auto-generated by `src/make_results.py`. Shows the original 100-image run and the "
      "corrected full-dataset run side by side (see `docs/CORRECTIONS.md`)._\n")

    A("## 0. Correction summary\n")
    A("The original Stage 1a pull used `JacksonFischer_2020_BreastCancer(data_type=\"sce\")` "
      "with the **undocumented default `full_dataset=FALSE`**, which silently returns only the "
      "~100-image **Basel** subset. The corrected run uses `full_dataset=TRUE` (Basel + "
      "**Zurich** cohorts). See `docs/CORRECTIONS.md` and `docs/marker_overlap.md`.\n")

    A("## 1. Datasets used (after filtering)\n")
    def num(x):
        return f"{x:,}" if isinstance(x, (int, float)) else "?"
    A(f"| | Original (Basel subset) | Corrected (full: Basel+Zurich) |")
    A(f"|---|---|---|")
    A(f"| Jackson pretrain slides | {old_jmeta.get('n_images','?')} | **{jmeta.get('n_images','?')}** |")
    A(f"| Jackson cells | {num(old_jmeta.get('n_cells'))} | **{num(jmeta.get('n_cells'))}** |")
    A(f"| Jackson markers used | 33 | **{n_jmarkers}** |")
    A(f"| Moldoveanu slides (eval) | 30 | 30 |")
    A("")
    A(f"- **Moldoveanu (unchanged):** {n_mold} slides = 30 patients, **{n_resp} responders / "
      f"{n_non} non-responders**, 35 markers, 112,497 cells. Evaluation = leave-one-patient-out.")
    A(f"- **Marker panel overlap (Jackson∩Moldoveanu): only {ov_shared} shared proteins, "
      f"{ov_jac}% Jaccard** (`docs/marker_overlap.md`) — low semantic overlap, a structural "
      f"reason to expect weak cross-panel transfer regardless of pretraining corpus size.\n")

    A("## 2. Headline — GNN vs. Giuliani baseline (both runs)\n")
    A("| Model | Original (Basel, 100 img) AUROC [95% CI] | Corrected (full) AUROC [95% CI] |")
    A("|---|---|---|")
    A(f"| **Spatial-graph GNN** (pretrained + fine-tune) | {fmt(*auc(old['']))} | **{fmt(g_auc, g_ci)}** |")
    A(f"| **Giuliani baseline** (cross-PCF @10.5 µm) | {fmt(*bauc(old['']))} | **{fmt(b_auc, b_ci)}** |")
    A("")
    verdict = cur[""].get("verdict", "")
    A(f"**Corrected verdict: {verdict}.** " + (
        "The corrected GNN's lower CI clears the baseline." if clears else
        "Even with ~6× the pretraining corpus, the GNN does **not** beat the hand-crafted "
        "spatial statistic; the GNN CI still crosses 0.5 and overlaps the baseline.") + "\n")

    A("## 3. Ablations with bootstrap CIs (both runs)\n")
    A("2000× patient-resample bootstrap on every configuration (not just the headline).\n")
    A("| GNN configuration | Original AUROC [95% CI] | Corrected AUROC [95% CI] |")
    A("|---|---|---|")
    A(f"| Pretrained + fine-tune (headline) | {fmt(*auc(old['']))} | {fmt(*auc(cur['']))} |")
    A(f"| Pretrained trunk **frozen** | {fmt(*auc(old['frozen']))} | {fmt(*auc(cur['frozen']))} |")
    A(f"| **No pretraining** (random init) | {fmt(*auc(old['nopretrain']))} | {fmt(*auc(cur['nopretrain']))} |")
    A("")
    # distinguishability statement based on CI overlap of the corrected run
    cfg = {k: auc(cur[k]) for k in ["", "frozen", "nopretrain"]}
    def overlaps(a, b):
        (aa, ac), (ba, bc) = a, b
        if None in (ac[0], bc[0]):
            return True
        return not (ac[1] < bc[0] or bc[1] < ac[0])
    all_overlap = (overlaps(cfg[""], cfg["frozen"]) and overlaps(cfg[""], cfg["nopretrain"])
                   and overlaps(cfg["frozen"], cfg["nopretrain"]))
    A(("**All three corrected-run CIs mutually overlap** — the pretrained / frozen / "
       "no-pretrain ordering is **not statistically distinguishable**; it is within bootstrap "
       "noise. No claim that pretraining helps or hurts is supported." if all_overlap else
       "The corrected-run CIs do **not** all overlap — see the ordering above; the effect is "
       "distinguishable at the 95% level.") + "\n")

    A("## 4. Wall-clock (RTX PRO 6000 Blackwell)\n")
    A(f"- Pretraining (full Jackson, {pre_g} graphs, early-stop @ epoch {pre_ep}): **{pre_s} s**")
    A(f"- Fine-tune + LOPO eval (30 folds + baseline + 2000× bootstrap): **{ft_s} s**")
    A(f"- Counterfactual gradient search: **{cf_s} s**\n")

    A("## 5. Counterfactual findings — GATED\n")
    if clears:
        A("The corrected GNN AUROC clears the baseline, so these are reported as tentative "
          "findings (still correlational):\n")
        for i, f in enumerate(findings.get("findings", []), 1):
            A(f"{i}. {f}")
    else:
        A("> ⚠️ **These are NOT biological findings.** The counterfactual gradients were "
          "computed on a model whose corrected discrimination (§2) does **not** clear the "
          "baseline and whose CI crosses chance. They are recorded only as a mechanistic "
          "demonstration that the gradient search runs; they must not be interpreted as "
          "biology unless/until a model that beats the baseline is obtained.\n")
        A("<details><summary>Mechanistic output (do not cite as findings)</summary>\n")
        for i, f in enumerate(findings.get("findings", []), 1):
            A(f"{i}. {f}")
        A("\n</details>")
    A("")

    A("## 6. Reproduce\n")
    A("```bash\nbash scripts/build_env.sh   # once\nbash run_all.sh            # Stages 1..6 -> this file\n```")
    A("All scripts take `--seed` (default 42); runs logged to `results/run_log.jsonl`. "
      "Contract: `docs/data_schema.md`; corrections: `docs/CORRECTIONS.md`; panel overlap: "
      "`docs/marker_overlap.md`.")

    out = os.path.join(REPO, "RESULTS.md")
    open(out, "w").write("\n".join(L) + "\n")
    print("wrote", out, "| corrected GNN", fmt(g_auc, g_ci), "| baseline", fmt(b_auc, b_ci),
          "| clears baseline:", clears)


if __name__ == "__main__":
    main()
